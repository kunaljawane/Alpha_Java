public class TypeConversion {
    public static void main(String args[])
    {
        //type must be compatible
        //destination type must be greater than source size

        int a=20;
        long b=a; //converting int to long

        System.out.println(b);

        //type casting

        float x=23.12f;

        int y=(int)x;

        System.out.println(y);

        //it will eliminate the number after the decimal point

        //char to num
        //in java there is the particular number assigned to the character 

        char p='a';

        int num1=p;

        System.out.println(num1);

        //in this way we can print the number asociated with that charateer
        //b=98c=99d=100


        //java is the portable language while cpp is not





    }
    
}
